.org 0

	LDI R19, 0x00; INITIALIZE MULTIPLICAND 
	LDI R18, 0x0A
	LDI R17, 0x01
	LDI R16, 0x02 
	LDI R23, 0x00; INITIALIZE MULTIPLIER
	LDI R22, 0x01
	LDI R21, 0x0C
	LDI R20, 0x0D 
	LDI R31, 0x00; INITIALIZE RESULTS TO 0
	LDI R30, 0x00
	LDI R29, 0x00
	LDI R28, 0x00
	LDI R28, 0x00
	LDI R27, 0x00
	LDI R26, 0x00
	LDI R25, 0x00
	LDI R24, 0x00  
	;CHECK IF MULTIPLIER IS 0 (INCLUDES R22ZERO AND R23ZERO)
	CPSE R20, R0; IF R20==0 SKIP
	DEC R20
	BRNE MULTIPLY; IF R20 != 0 GO TO MULTIPLY
	CP R21, R0; CHECK IF R21==0    
	BREQ R22ZERO; GO TO R22ZERO IF R21==0
	DEC R21
	SER R20; SET R20 TO 255
	JMP MULTIPLY; BACK TO MULTIPLY
R22ZERO:		
	CP R22, R0  
	BREQ R23ZERO
	SER R20
	SER R21
	JMP MULTIPLY
R23ZERO:
	CP R23, R0
	BREQ ZERO
	DEC R23
	SER R20
	SER R21
	SER R22 
	;ADDING MULTIPLICAND AND RESULT VALUES    
MULTIPLY:
	ADD R24, R16
	ADC R25, R17
	ADC R26, R18
	ADC R27, R19
	ADC R28, R0
	ADC R29, R0
	ADC R30, R0
	ADC R31, R0  
	JMP R20_CHECK
	;CHECKING ALL MULTIPLIER REGISTERS FOR ==0
R20_CHECK:		
	CP R20, R0
	BREQ R21_CHECK
	DEC R20
	JMP MULTIPLY
R21_CHECK:
	CP R21, R0
	BREQ R22_CHECK
	DEC R21
	SER R20
	JMP MULTIPLY
R22_CHECK: 
	CP R22, R0
	BREQ R23_CHECK
	DEC R22
	SER R20
	SER R21
	JMP MULTIPLY
R23_CHECK: 
	CP R23, R0
	BREQ ZERO
	DEC R23
	SER R20
	SER R21
	SER R22
	JMP MULTIPLY
ZERO:
END: JMP END  			
